            2. **Informações de Uso:** Dados de acesso, como endereço IP, tipo de navegador, páginas visitadas, tempo
            gasto no site e outras informações coletadas por meio de cookies e tecnologias semelhantes. <br><br>
            3. **Informações de Pagamento:** Caso haja transações financeiras, informações de pagamento podem ser
            coletadas para processar pagamentos de serviços ou produtos oferecidos. <br><br>

            **Uso das Informações** <br><br>

            As informações coletadas são utilizadas para os seguintes propósitos: <br><br>

            1. **Fornecimento de Serviços:** Para operar a biblioteca digital, oferecer serviços, processar transações e
            fornecer suporte ao usuário. <br><br>
            2. **Melhoria dos Serviços:** Para analisar o desempenho da plataforma, entender o comportamento do usuário
            e aprimorar a experiência do usuário. <br><br>
            3. **Comunicação:** Para enviar informações relevantes sobre a biblioteca digital, atualizações, newsletters
            ou comunicações relacionadas aos serviços oferecidos. <br><br>
            4. **Marketing:** Para enviar materiais de marketing ou promoções sobre novos recursos, eventos ou produtos,
            caso o usuário tenha consentido. <br><br>

            **Compartilhamento de Informações** <br><br>

            Não compartilhamos informações pessoais dos usuários, exceto nas seguintes circunstâncias: <br><br>

            1. **Parceiros e Prestadores de Serviços:** Podemos compartilhar informações com terceiros que nos auxiliam
            na prestação de serviços, como processamento de pagamentos ou análise de dados. Esses terceiros são
            obrigados a manter a confidencialidade das informações. <br><br>
            2. **Requisitos Legais:** Podemos divulgar informações pessoais caso seja necessário cumprir com obrigações
            legais, responder a solicitações legais ou proteger nossos direitos legais. <br><br>

            **Segurança das Informações** <br><br>

            Implementamos medidas de segurança para proteger as informações pessoais dos usuários contra acesso não
            autorizado, alteração, divulgação ou destruição. <br><br>

            **Cookies e Tecnologias Semelhantes** <br><br>

            Utilizamos cookies e tecnologias similares para melhorar a experiência do usuário, analisar tendências,
            administrar o site e rastrear a movimentação dos usuários pelo site. <br><br>

            **Seus Direitos** <br><br>

            Os usuários têm direito a acessar, corrigir, atualizar ou solicitar a exclusão de suas informações pessoais.
            Também podem optar por não receber comunicações de marketing. <br><br>

